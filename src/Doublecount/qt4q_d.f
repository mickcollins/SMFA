      subroutine qt4q_d(q1,q2,t5,deqt4q)

      implicit double precision(a-h,o-z)

      dimension q1(3,3),q2(3,3),t5(3,3,3,3,3),deqt4q(3)

c  this subroutine evaluates the quadrupole-quadrupole
c  electrostatic interaction

      dimension a(3,15),b(15)

      a(1,1)=t5(1,1,1,1,1)
      a(1,2)=t5(1,1,1,1,2)
      a(1,3)=t5(1,1,1,1,3)
      a(1,4)=t5(1,1,1,2,2)
      a(1,5)=t5(1,1,1,2,3)
      a(1,6)=t5(1,1,1,3,3)
      a(1,7)=t5(1,1,2,2,2)
      a(1,8)=t5(1,1,2,2,3)
      a(1,9)=t5(1,1,2,3,3)
      a(1,10)=t5(1,1,3,3,3)
      a(1,11)=t5(1,2,2,2,2)
      a(1,12)=t5(1,2,2,2,3)
      a(1,13)=t5(1,2,2,3,3)
      a(1,14)=t5(1,2,3,3,3)
      a(1,15)=t5(1,3,3,3,3)

      a(2,1)=t5(1,1,1,1,2)
      a(2,2)=t5(1,1,1,2,2)
      a(2,3)=t5(1,1,1,2,3)
      a(2,4)=t5(1,1,2,2,2)
      a(2,5)=t5(1,1,2,2,3)
      a(2,6)=t5(1,1,2,3,3)
      a(2,7)=t5(1,2,2,2,2)
      a(2,8)=t5(1,2,2,2,3)
      a(2,9)=t5(1,2,2,3,3)
      a(2,10)=t5(1,2,3,3,3)
      a(2,11)=t5(2,2,2,2,2)
      a(2,12)=t5(2,2,2,2,3)
      a(2,13)=t5(2,2,2,3,3)
      a(2,14)=t5(2,2,3,3,3)
      a(2,15)=t5(2,3,3,3,3)

      a(3,1)=t5(1,1,1,1,3)
      a(3,2)=t5(1,1,1,2,3)
      a(3,3)=t5(1,1,1,3,3)
      a(3,4)=t5(1,1,2,2,3)
      a(3,5)=t5(1,1,2,3,3)
      a(3,6)=t5(1,1,3,3,3)
      a(3,7)=t5(1,2,2,2,3)
      a(3,8)=t5(1,2,2,3,3)
      a(3,9)=t5(1,2,3,3,3)
      a(3,10)=t5(1,3,3,3,3)
      a(3,11)=t5(2,2,2,2,3)
      a(3,12)=t5(2,2,2,3,3)
      a(3,13)=t5(2,2,3,3,3)
      a(3,14)=t5(2,3,3,3,3)
      a(3,15)=t5(3,3,3,3,3)

      b(1)=q1(1,1)*q2(1,1)
      b(2)=2.d0*(q1(1,2)*q2(1,1)+q1(1,1)*q2(1,2))
      b(3)=2.d0*(q1(1,3)*q2(1,1)+q1(1,1)*q2(1,3))
      b(4)=q1(2,2)*q2(1,1)+q1(1,1)*q2(2,2)+4.d0*q1(1,2)*q2(1,2)
      b(5)=2.d0*(q1(1,1)*q2(2,3)+q1(2,3)*q2(1,1)+2.d0*(
     . q1(1,2)*q2(1,3)+q1(1,3)*q2(1,2)))
      b(6)=q1(3,3)*q2(1,1)+q1(1,1)*q2(3,3)+4.d0*q1(1,3)*q2(1,3)
      b(7)=2.d0*(q1(1,2)*q2(2,2)+q1(2,2)*q2(1,2))
      b(8)=2.d0*(q1(1,3)*q2(2,2)+q1(2,2)*q2(1,3)+2.d0*(
     . q1(1,2)*q2(2,3)+q1(2,3)*q2(1,2)))
      b(9)=2.d0*(q1(1,2)*q2(3,3)+q1(3,3)*q2(1,2)+2.d0*(
     . q1(1,3)*q2(2,3)+q1(2,3)*q2(1,3)))
      b(10)=2.d0*(q1(1,3)*q2(3,3)+q1(3,3)*q2(1,3))
      b(11)=q1(2,2)*q2(2,2)
      b(12)=2.d0*(q1(2,3)*q2(2,2)+q1(2,2)*q2(2,3))
      b(13)=q1(2,2)*q2(3,3)+q1(3,3)*q2(2,2)+4.d0*q1(2,3)*q2(2,3)
      b(14)=2.d0*(q1(2,3)*q2(3,3)+q1(3,3)*q2(2,3))
      b(15)=q1(3,3)*q2(3,3)

      deqt4q(1)=a(1,1)*b(1)+a(1,2)*b(2)+a(1,3)*b(3)+a(1,4)*b(4)+
     .      a(1,5)*b(5)+a(1,6)*b(6)+a(1,7)*b(7)+a(1,8)*b(8)+a(1,9)*b(9)+
     .      a(1,10)*b(10)+a(1,11)*b(11)+a(1,12)*b(12)+a(1,13)*b(13)+
     .      a(1,14)*b(14)+a(1,15)*b(15)

      deqt4q(2)=a(2,1)*b(1)+a(2,2)*b(2)+a(2,3)*b(3)+a(2,4)*b(4)+
     .      a(2,5)*b(5)+a(2,6)*b(6)+a(2,7)*b(7)+a(2,8)*b(8)+a(2,9)*b(9)+
     .      a(2,10)*b(10)+a(2,11)*b(11)+a(2,12)*b(12)+a(2,13)*b(13)+
     .      a(2,14)*b(14)+a(2,15)*b(15)

      deqt4q(3)=a(3,1)*b(1)+a(3,2)*b(2)+a(3,3)*b(3)+a(3,4)*b(4)+
     .      a(3,5)*b(5)+a(3,6)*b(6)+a(3,7)*b(7)+a(3,8)*b(8)+a(3,9)*b(9)+
     .      a(3,10)*b(10)+a(3,11)*b(11)+a(3,12)*b(12)+a(3,13)*b(13)+
     .      a(3,14)*b(14)+a(3,15)*b(15)

      return
      end

