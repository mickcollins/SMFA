      dect4h(1)=c1*(a(1,1)+a(1,2)+a(1,3)+a(1,4)+a(1,5)+a(1,6)+
     .          a(1,7)+a(1,8)+a(1,9)+a(1,10)+a(1,11)+a(1,12)+
