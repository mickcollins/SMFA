      implicit double precision(a-h,o-z)

      dimension h2(3,3,3,3),t5(3,3,3,3,3),dect4h(3)

c  this subroutine evaluates the quadrupole-quadrupole
c  electrostatic interaction

      dimension a(3,15),b(15)

      a(1,1)=t5(1,1,1,1,1)*h2(1,1,1,1)
      a(1,2)=t5(1,1,1,1,2)*h2(1,1,1,2)*4.d0
      a(1,3)=t5(1,1,1,1,3)*h2(1,1,1,3)*4.d0
      a(1,4)=t5(1,1,1,2,2)*h2(1,1,2,2)*6.d0
      a(1,5)=t5(1,1,1,2,3)*h2(1,1,2,3)*12.d0
      a(1,6)=t5(1,1,1,3,3)*h2(1,1,3,3)*6.d0
      a(1,7)=t5(1,1,2,2,2)*h2(1,2,2,2)*4.d0
      a(1,8)=t5(1,1,2,2,3)*h2(1,2,2,3)*12.d0
      a(1,9)=t5(1,1,2,3,3)*h2(1,2,3,3)*12.d0
      a(1,10)=t5(1,1,3,3,3)*h2(1,3,3,3)*4.d0
      a(1,11)=t5(1,2,2,2,2)*h2(2,2,2,2)
      a(1,12)=t5(1,2,2,2,3)*h2(2,2,2,3)*4.d0
      a(1,13)=t5(1,2,2,3,3)*h2(2,2,3,3)*6.d0
      a(1,14)=t5(1,2,3,3,3)*h2(2,3,3,3)*4.d0
      a(1,15)=t5(1,3,3,3,3)*h2(3,3,3,3)

      a(2,1)=t5(1,1,1,1,2)*h2(1,1,1,1)
      a(2,2)=t5(1,1,1,2,2)*h2(1,1,1,2)*4.d0
      a(2,3)=t5(1,1,1,2,3)*h2(1,1,1,3)*4.d0
      a(2,4)=t5(1,1,2,2,2)*h2(1,1,2,2)*6.d0
      a(2,5)=t5(1,1,2,2,3)*h2(1,1,2,3)*12.d0
      a(2,6)=t5(1,1,2,3,3)*h2(1,1,3,3)*6.d0
      a(2,7)=t5(1,2,2,2,2)*h2(1,2,2,2)*4.d0
      a(2,8)=t5(1,2,2,2,3)*h2(1,2,2,3)*12.d0
      a(2,9)=t5(1,2,2,3,3)*h2(1,2,3,3)*12.d0
      a(2,10)=t5(1,2,3,3,3)*h2(1,3,3,3)*4.d0
      a(2,11)=t5(2,2,2,2,2)*h2(2,2,2,2)
      a(2,12)=t5(2,2,2,2,3)*h2(2,2,2,3)*4.d0
      a(2,13)=t5(2,2,2,3,3)*h2(2,2,3,3)*6.d0
      a(2,14)=t5(2,2,3,3,3)*h2(2,3,3,3)*4.d0
      a(2,15)=t5(2,3,3,3,3)*h2(3,3,3,3)

      a(3,1)=t5(1,1,1,1,3)*h2(1,1,1,1)
      a(3,2)=t5(1,1,1,2,3)*h2(1,1,1,2)*4.d0
      a(3,3)=t5(1,1,1,3,3)*h2(1,1,1,3)*4.d0
      a(3,4)=t5(1,1,2,2,3)*h2(1,1,2,2)*6.d0
      a(3,5)=t5(1,1,2,3,3)*h2(1,1,2,3)*12.d0
      a(3,6)=t5(1,1,3,3,3)*h2(1,1,3,3)*6.d0
      a(3,7)=t5(1,2,2,2,3)*h2(1,2,2,2)*4.d0
      a(3,8)=t5(1,2,2,3,3)*h2(1,2,2,3)*12.d0
      a(3,9)=t5(1,2,3,3,3)*h2(1,2,3,3)*12.d0
      a(3,10)=t5(1,3,3,3,3)*h2(1,3,3,3)*4.d0
      a(3,11)=t5(2,2,2,2,3)*h2(2,2,2,2)
      a(3,12)=t5(2,2,2,3,3)*h2(2,2,2,3)*4.d0
      a(3,13)=t5(2,2,3,3,3)*h2(2,2,3,3)*6.d0
      a(3,14)=t5(2,3,3,3,3)*h2(2,3,3,3)*4.d0
