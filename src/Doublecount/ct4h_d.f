     .          a(2,7)+a(2,8)+a(2,9)+a(2,10)+a(2,11)+a(2,12)+
     .          a(2,13)+a(2,14)+a(2,15))

      dect4h(3)=c1*(a(3,1)+a(3,2)+a(3,3)+a(3,4)+a(3,5)+a(3,6)+
     .          a(3,7)+a(3,8)+a(3,9)+a(3,10)+a(3,11)+a(3,12)+
     .          a(3,13)+a(3,14)+a(3,15))

      return
      end

